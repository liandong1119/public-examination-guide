const insertTemplate = (template) => {
  if (monacoEditor.value) {
    const selection = monacoEditor.value.getSelection()
    monacoEditor.value.executeEdits('', [{
      range: selection,
      text: template.latex
    }])
    monacoEditor.value.focus()
  }
}

