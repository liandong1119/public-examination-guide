}

const getFormulaTypeLabel = (type) => {
  const labels = {
    algebra: '‰ª£Êï∞ÊñπÁ®ã',
    geometry: 'Âá†‰ΩïÂÖ¨Âºè',
    probability: 'Ê¶ÇÁéáÁªüËÆ°',
    calculus: 'ÂæÆÁßØÂàÜ'
